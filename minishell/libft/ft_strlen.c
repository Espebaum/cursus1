#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	cnt;

	cnt = 0;
	if (!s || s[0] == '\0')
		return (0);
	while (s && *s)
	{
		cnt++;
		s++;
	}
	return (cnt);
}
